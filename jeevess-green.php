<h5>Organic, Nature Friendly, Eco Sustainable Zone</h5>
<p>Within the asymmetrical area spanning 12 acres of Jeevess, there are tea plants and coffee plants aged more than hundred years. The main attraction of Jeevess is the same plush green tea plantation and coffee plantation. These plants stay high and tall all due to proper cutting and maintenance and protection. It is not just the coffee and tea but the entire Jeevess cultivation is in organic manner. The organic natural way of treatment is effective and healthier as compared to chemicals and chemical fertilizers. By using this tea and coffee, Jeevess produces organic coffee, tea and other medicinal products
</p>
<p>One of Jeevess’ main projects is to promote Jeevess as a green, organic, nature friendly, eco sustainable zone. To attain this, we have given a lot of our attention. With causing any damage to the trees and tea plants while protecting them, we have built the buildings and done the constructions. To separate gardens and borders, we have used the bio fencing method. A natural ecosystem itself is maintained here. As there are plenty of medicinal plants, fruit and flowers, different types of birds, peacocks, butterflies, bees and monkeys visit Jeeves frequently and they live with humans in a harmonious manner.</p>
<p>Jeevess is a completely plastic waste free property. Nowadays, a large share of the earth’s fertility has been affected by plastic wastes. Instead of single used plastic, other alternative organic methods are relied upon. Solar panels and bio gas plants have been set up to strengthen this project</p>
<p>During these times of unavailability of fuels and energy crisis, Jeevess intends to explore and utilise solar energy and energy from biofuel to the maximum. As there is plenty of rainfall available, to not waste the rainwater and collect it, we have constructed a large natural pond. By following this way, water will be available for the whole year and can be used for various purposes without any scarcity.
</p>
<p>For medicinal plant cultivation and other agricultural activities, a lot of water is required. However, Jeevess utilizes STP purified, colourless, odourless recycled water for the irrigation purposes. Latest methods have been adopted to reap the harvest from medicinal plants without harming them.</p>
<p>Another medicinal product from Jeevess is the Jeevess pure honey. The beekeeping project has been implemented under the supervision of trained experts. For the honeybees to collect honey, the required amount of flowers is available in abundance. The honey collected from the flowers in the medicinal plants have an entirely high medicinal value. This is how the Jeevess honey is made. Also, the necessary dairy products like milk, ghee are collected from farmers who maintain cows of higher breeds. Since the soil in Jeevess is very fertile, the maximum portion of land is used for cultivation. In order to support greenery and to prevent soil erosion, most of the area in the property is filled with buffalo grass as it preserves the fertility of the soil.</p>

