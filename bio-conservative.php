<h5>Bio-fencing</h5>    
<p>In the property, we have used those medicinal plants which are used for medicine preparations and treatments are selected to do the fencing. In this way, the garden sections are   differentiated and to separate the cultivation areas from the pathways. This is the best way to do cultivation in less space with useful plants for fencing. </p>
