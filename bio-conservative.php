    <div class="bio_cont_row">
<h5>RET-Rare Endangered and Threatened Plants Cultivation and Conservation</h5>   <p>There are plenty of medicinal plants that are on the verge of extinction at present.  Because of this, a lot of chronic and acute diseases that could have been treated easily have to be treated with the aid of other options. In the RET category, different medicinal plants, trees, water plants, climbers, grasses are included.  

Jeevess has planted these medicinal plants after collecting them from different people and different places. We also develop them and protect them from damages. </p>
</div>
    
    <div class="bio_cont_row">
<h5>Medicinal Grasses – Cultivation</h5>    
<p>We are nurturing different types of medicinal grasses. These are not only for the purpose of treatments, but have a big role in Ecological sustenance.</p>
</div>
    
    <div class="bio_cont_row">
<h5>Water Loving Medicinal Plants Cultivation and Conservation</h5>    
<p>In Jeevess, the popular medicinal plants that are used in  medicines aplenty which grow in water blocks and marsh lands are cultivated, protected and used for medicine manufacturing. Some of the examples include sweet flags (Acorus calamus), kokilaksha (Hygrophila auriculata), Brahmi (Bacopa monnieri), neer thippali (Lippa nodiflora). In them both the common and rare varieties are included.</p>
</div>
    
    <div class="bio_cont_row">
<h5>Sustainable Harvesting Methods</h5>    
<p>In Ayurveda, in a plant generally there are ten plant parts  which are used for medicinal purposes like fruit, seed, leaves, roots, bark etc, So when we collect the medicines, we collect the necessary items without damaging the entire plant or leaving enough to for sustainable cultivation by relying on traditional and scientific methods. If we use the plants completely, we will either collect the seeds or cut new branches from them and use them for re cultivation. </p>
<p>We have set up a poly house facility for the same. For some plants, the stem bark is used but for some others, the used ones are the roots. In this way, the trained experts collect the necessary parts and adopt the measures to protect the mother plant. In this manner, we can take yield from the same plant/tree for a long period. </p>
</div>
    
    <div class="bio_cont_row">
<h5>Nakshathra Sasyangal (planet plants)</h5>    
<p>In the ayurvedic branch of medicine, the influence of stars and planetary positions on the health and diseases of human beings are mentioned. For instance, the effect of sun and moon on the human body is known to us. Similarly, certain medicinal plants have been connected with some planets and planetary positions. In this manner the important planets and  related medicinal plants  are known by the name ‘Nakshathra Sasyangal’. ‘Nakshathra’ means star and ‘sasya’ means plant. They are known by Malayalam names locally. These medicinal plants are specially planted in a reserved area. It is believed people born to the particular star need to take care of that plant.</p>
</div>
    
