<h5>Healing Plants</h5>    
<p>There is a category of medicinal plants called healing plants to treat and cure specific diseases planted in particular.  they are used in traditional folklore treatments   but are either extinct or are in a way that it is not available nowadays. Hence it is the necessity of this age collect and protect the medicinal plants</p>
