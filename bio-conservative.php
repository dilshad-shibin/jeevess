<p>One of the major specialties of Jeevess being the medicinal plant cultivation in the Hospital vicinity. The main challenge faced by the Ayurvedic medicine manufacturing sector is the scarcity of raw drugs. As an attempt, we are using Wayanad’s fertile soil to cultivate herbs required for manufacturing medicines in our production unit.  We are using herbs grown in our garden for manufacturing medicines for our patients as much as possible. To effectively utilize all spaces efficiently, we employ different ways of cultivating more plants like biofencing, intercropping etc. </p>
