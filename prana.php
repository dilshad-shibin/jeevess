<p><b>Organs that may be affected by COVID-19 include:</b></p>
<p><b>Heart:</b> Imaging tests taken months after recovery from COVID-19 have shown lasting damage to the heart muscle, even in people who experienced only mild COVID-19 symptoms. This may increase the risk of heart failure or other heart complications in the future.</p>
<p><b>Lungs:</b> The type of pneumonia often associated with COVID-19 can cause long-standing damage to the tiny air sacs (alveoli) in the lungs. The resulting scar tissue can lead to long-term breathing problems.</p>
<p><b>Brain:</b> Even in young people, COVID-19 can cause strokes, seizures and Guillain-Barre syndrome — a condition that causes temporary paralysis. COVID-19 may also increase the risk of developing Parkinson's disease and Alzheimer's disease.</p>

<br>
<p><b>Other long term issues:</b></p>
<p><b>Blood clots and blood vessel problems:</b>
COVID-19 can make blood cells more likely to clump up and form clots. While large clots can cause heart attacks and strokes, much of the heart damage caused by COVID-19 is believed to stem from very small clots that block tiny blood vessels (capillaries) in the heart muscle.
Other parts of the body affected by blood clots include the lungs, legs, liver and kidneys. COVID-19 can also weaken blood vessels and cause them to leak, which contributes to potentially long-lasting problems with the liver and kidneys.
</p>
<p><b>Problems with mood and fatigue:</b>
People who have severe symptoms of COVID-19 often have to be treated in a hospital's intensive care unit, with mechanical assistance such as ventilators to breathe. Simply surviving this experience can make a person more likely to later develop post-traumatic stress syndrome, depression and anxiety.
Because it's difficult to predict long-term outcomes from the new COVID-19 virus, scientists are looking at the long-term effects seen in related viruses, such as the virus that causes severe acute respiratory syndrome (SARS).
Many people who have recovered from SARS have gone on to develop chronic fatigue syndrome, a complex disorder characterized by extreme fatigue that worsens with physical or mental activity, but doesn't improve with rest. The same may be true for people who have had COVID-19.
</p>
