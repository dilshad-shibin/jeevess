<p>What it takes to be an athlete? It is different from person to person, sp gvort to sport, region to region and the mindsets. 
Ūrja, our physical fitness development program uses the integrated approach of traditional Kalari martial art practices, the wisdom of Ayurveda, Yoga and the contemporary science related to sports and fitness. 
The program caters to three categories, first for aspiring children, second, active sportspersons and those who reduced their activity and needs to change the activity as per the physiological changes. </p>

<p>From Ayurvedic perspective, the physical, physiological and psychological requirements to become a sports person varies from the type of the sports they are engaged, the Ayurvedic body types, the mind types and age group. </p>

<p>Our plan includes a comprehensive assessment of physical and physiological status of body from ayurvedic perspective. Based on the evaluation, we make custom plans for each person. The plan is delivered in to three categories; the treatments, activities and diet.  </p>

<p><b>Treatments and medications:</b></p>

<p>Depending on the body type and age group we do a set of preparatory treatments called Rookshana , in which we employ treatments like udwartahana (powder massage), podikizhi, dhanyakizhi, (special pouch massages) etc. with appropriate herbs and mediums. In the second phase to enhance “internal circulation and metabolism” we  do oil based and sweating treatments like , elakizhi (herbal pouch massage), narangakizhi (special pouch massge with lemon), pizhichil (pouring of medicated oil), etc. In the third phase, we do nourishing and rejuvenating brimhana treatments which may include, Njavarakizhi, Mamasakizhi, Mutta kizhi, etc. 
We also do chavitty uzhichil (massage with feet), kalari massage, and some marma (vital points) stimulations and applications as per conditions. In some cases we may do some Panchakarma procedures too. 
During the different phases of treatment we give different internal medications in line with the treatments and body types. These medication can be decoctions, fermented preparations, pastes etc.</p>

<p><i>Please do note that athletes who are planning for any immediate completion, we may not give any internal medicines to prevent any possible unknown interventions with hormones and chemicals in the body which are not desirous as per regulations. </i></p>

<p><b>Activities and physical trainings :</b> </p>

<p>Kalari practices and yoga are the key methods we use for the program. 
The various martial arts steps to improve body’s flexibility, coordination, reflexes and endurance like Vadivukal, maipayattu are taught to the athletes as per their condition and requirements.  The use of special oils used in martial arts to treat chronic injuries are used and also oils and medications for strengthening the body. </p>

<p>The breathing techniques with different pranayama and yoga postures will help to build the endurance and focus of the body as well as to improve the faster body recovery from fatigue caused by sports activity. Apart from that, it will make mind more focused and centered in the competitive events. </p>

<p><b>Diet :</b></p>

<p>Diet is a critical part of any sports training, one need to have the sufficient energy and nutrients as per the activities and physical requirements. In Ayurveda we assess the prakruthy or body type, based on the individual’s body type, the dietary requirements are different. The other critical area we consider is the concept of Agni - one’s digestive capacity, is assessed and the food is prescribed accordingly. What it means is that, even if you calculate the right nutrients required, but if the body is not able to absorb it, then there will be a mismatch in performance.  <br>
The diet varies as per the treatment requirement. We also give recommendations to follow back home.<br>
The diet may include some medicated dishes from the classical textbooks and traditional practices as per the condition. </p>

<p>Notes:</p>

<p>program is meant for professional and also for those enthusiast who wants to keep their body fit. <br>
The fitness requirements for a 100 meter sprint and marathon are different and even different for someone who’s going for power lifting or wrestling.  The requirement also varies for a growing child athlete and a grownup professional player. The other thing to be noted is that, in some sports person after long trainings and carrier, once they reduce the active life, then there are lot of challenges to be addressed. In some physically active people, the repetitive injury can cause different health issue. In Urja we address those attributes which will enhance your physical capabilities. <br>
Depending on the treatments undergone, if one needs to undergo Panchakarma treatments, those may be asked to take absolute rest during treatments. If you are going mainly for non-Panchakarma treatments, you may do some physical training as per doctor’s advice. </p>

