<h5>Jeevess Bheshajagara - Manufacturing Unit</h5>
<p>In the domain of Ayurvedic medicine, the concept of tri-sutras has been mentioned in the texts. These are Hetu (Causative Factor), Linga (Sign & Symptoms) and Oushada (treatment). Due to this, oushada is given large importance while doing the treatment. The word bhaishajyam (medicine) means that which helps the bhishak (doctor) to conduct the treatment. Oushada is greatly responsible when it comes to alleviate disease and to maintain and foster good health.</p>
<p>Bhaishajya Kalpana (Ayurvedic Pharmaceutics) is the branch of Ayurveda that explains about medicine manufacturing and the methods related to it. Similarly, Ayurveda mentions how ideal medicine manufacturing units have to be built.</p>
<p>In this way, as per the specifications in the Ayurvedic texts and the modern infrastructure, we have established our medicine manufacturing unit which is Jeevess Bheshajagara. The Manufacturing complex is built in a 3200 square feet area. The medicines are manufactured under the supervision of highly skilled experts who have many years of experience in this domain.</p>
<p>We source most of the raw drugs required for medicine preparation from our garden itself. We made improvisations in the traditional preparation to improve efficiency.</p>
<p>Separate areas are allocated for receiving raw drugs, processing, storage, chopping, powdering etc. We have our in-house quality control facility for conducting all the first line quality tests. The fermentation of Arishtam (fermented medicinal preparations) is done in a traditional fermentation house, which is also housed in the Bheshajagara complex.</p>
<p>We make custom formulations for patients as per their condition when required. In our Beshajagara, we prepare different types of Ayurvedic medicinal formulations like Kashayam (decoctions), arishtam (fermented medicinal preparations), choornam (medicinal powder preparations), Gulika (tablets), tailam (medicated oils), gritham (medicated ghee), lehyam (lickable paste-like formulations), etc.</p>

